int		ft_atoi(char *s)
{
	int neg;
	int total;

	total = 0;
	neg = -1;
	if (*s == '\0')
		return (0);
	while (*s == ' ' || (*s >= 9 && *s <= 13))
	{
		s++;
	}
	if (*s == '-')
		neg = 1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		total *= 10;
		total -= *s - '0';
		s++;
	}
	return (total * neg);
}
